package Model.BasicTypes;

public abstract class Plant {

    Double nitrogenNeeds;
    Double phosphorusNeeds;
    Double kaliumNeeds;

    Double magnesiumNeeds;
    Double calciumNeeds;

    Double ironNeeds;
    Double zincNeeds;
    Double boronNeeds;
    Double copperNeeds;
    Double natriumNeeds;
    Double chlorineNeeds;
    Double manganeseNeeds;
    Double molybdenumNeeds;

    Double idealPH;
    Double idealEC;

    public Double getNitrogenNeeds() {
        return nitrogenNeeds;
    }

    public Double getPhosphorusNeeds() {
        return phosphorusNeeds;
    }

    public Double getKaliumNeeds() {
        return kaliumNeeds;
    }

    public Double getMagnesiumNeeds() {
        return magnesiumNeeds;
    }

    public Double getCalciumNeeds() {
        return calciumNeeds;
    }

    public Double getIronNeeds() {
        return ironNeeds;
    }

    public Double getZincNeeds() {
        return zincNeeds;
    }

    public Double getBoronNeeds() {
        return boronNeeds;
    }

    public Double getCopperNeeds() {
        return copperNeeds;
    }

    public Double getNatriumNeeds() {
        return natriumNeeds;
    }

    public Double getChlorineNeeds() {
        return chlorineNeeds;
    }

    public Double getManganeseNeeds() {
        return manganeseNeeds;
    }

    public Double getMolybdenumNeeds() {
        return molybdenumNeeds;
    }

    public Double getIdealPH() {
        return idealPH;
    }

    public Double getIdealEC() {
        return idealEC;
    }
}
